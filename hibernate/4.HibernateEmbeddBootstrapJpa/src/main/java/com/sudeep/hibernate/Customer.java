package com.sudeep.hibernate;

import javax.persistence.Entity;

@Entity(name="Customer")
public class Customer {

	private String name;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
}
